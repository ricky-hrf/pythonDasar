print("======= PENGGUNAAN SYMBOL =======  \n")

# penggunaan %c
print("Karakter pertama: %c" % 'B')

# penggunaan %s
print("Nama saya %s" % 'Budi')

# penggunaan %d
print("Umur saya %d tahun" % 20)

# penggunaan %f
print("Nilai rata-rata saya %.2f" % 85.5)

# penggunaan %x
print("Nilai heksadesimal dari 255 adalah %x" % 255)

# penggunaan %o
print("Nilai oktal dari 255 adalah %o" % 255)